// 프랫 파싱은 어떻게 동작하는가
// prefixParseFns nud-null denotation
// infixParseFn led left denotation
// 1 + 2  + 3;
// 가장 어려운 문제는 AST 노드들을 규칙에 맞게 중첩하는 것이다.
// 그룹표현식은 별도로 AST 타입을 정의할 필요가 없다.
// 따라서 기존 AST를 변경하지 않고도 그룹 표현삭을 파싱 가능
// myArray[0]에 있는 중위연산자'['를  myArray를 왼쪽 피연산자, 0을 오른쪽 피연산자로 처리해야 한다.
func TestOperatorPrecedenceParsing(t *testing.T) {
	tests := []struct {
		input    string
		expected string
	}{
		{"-a * b", "((-a) * b)"},
		{"!-a", "(!(-a))"},
		{"a + b + c", "((a + b) + c)"},
		{"a + b - c", "((a + b) - c)"},
		{"a * b * c", "((a * b) * c)"},
		{"a * b / c", "((a * b) / c)"},
		{"a + b / c", "(a + (b / c))"},
		{"a + b * c + d / e - f", "(((a + (b * c)) + (d / e)) - f)"},
		{"3 + 4; -5 * 5", "(3 + 4)((-5) * 5)"},

		{"5 > 4 == 3 < 4", "((5 > 4) == (3 < 4))"},
		{"5 < 4 != 3 > 4", "((5 < 4) != (3 > 4))"},
		{"3 + 4 * 5 == 3 * 1 + 4 * 5", "((3 + (4 * 5)) == ((3 * 1) + (4 * 5))"},

		{"true", "true"},
		{"false", "false"},
		{"3 > 5 == false", "((3 > 5) == false)"},
		{"3 < 5 == true", "((3 < 5) == true)"},

		{"a * [1, 2, 3, 4][b * c] *d", "((a * ([(1, 2, 3, 4)][(b * c)])) * d)"},
		{"add(a * b[2], b[1], 2 * [1, 2][1])", "add((a * (b[2]), (b[1]), (2 * ([1, 2][1])))"},
	}
	for _, tt := range tests {

		l := lexer.New(tt.input)
		p := New(l)
		program := p.ParseProgram()
		checkParserErrors(t, p)

		actual := program.String()
		if actual != tt.expected {
			t.Errorf("expected=%q, got=%q", tt.expected, actual)
		}
	}
}

